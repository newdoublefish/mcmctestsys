unsigned short crc_ccitt(unsigned char *q, int len)
{
unsigned short crc = 0;

while (len-- > 0)
crc = crcTable[(crc >> 8 ^ *q++) & 0xff] ^ (crc << 8);
return crc;
}


unsigned short CalculateCrc(unsigned char *q,int len){
	unsigned short crc = 65535;
    unsigned char tableIndex;

    for (int i = 0; i < len; i++)
   {
                tableIndex = (unsigned char)(crc ^ q[i]);
                crc >>= 8;
                crc ^= crcTable[tableIndex];
   }
   return crc;
}

unsigned short caculate_crc16(unsigned char *q, int len)
{
   unsigned int crc = 0;
   unsigned char crc_H8;
   unsigned char *ptr = q;

   while( len-- ) {
      crc_H8 = (unsigned char)(crc >> 8);
      crc = crc << 8;
      crc = crc ^ crcTable[ crc_H8 ^ *ptr];
      ptr++;
   }
   return crc;
}
